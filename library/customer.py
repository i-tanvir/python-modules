class Customer:
    def __init__(self, name):
        name = self.name
        accounts = []

    def add_account(account):
        accounts.append(account)

    def show_accounts():
        if len(accounts) == 0:
            print(f"{name} has no accounts.")
        elif len(accounts) > 0:
            for account in accounts:
                print(f"- Account {account.account_number}: £{account.balance} \n")
        else:
            pass
    